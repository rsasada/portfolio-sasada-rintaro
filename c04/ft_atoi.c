int		plus(int u)
{
	int z;

	z = 0;
	if (u % 2 == 1)
	{
		z = -1;
	}
	else if (u % 2 == 0)
	{
		z = 1;
	}
	return (z);
}

int		ft_atoi(char *str)
{
	int n[4];

	n[0] = 0;
	n[1] = 0;
	n[2] = 0;
	n[3] = 0;
	while (str[n[0]] == 0x20)
	{
		n[0]++;
	}
	while (str[n[0]] == '-' || str[n[0]] == '+')
	{
		if (str[n[0]] == '-')
		{
			n[3]++;
		}
		n[0]++;
	}
	n[1] = plus(n[3]);
	while (str[n[0]] >= '0' && str[n[0]] <= '9')
	{
		n[2] = 10 * n[2] + (str[n[0]] - '0');
		n[0]++;
	}
	return (n[2] * n[1]);
}
